def row(seq: int, entry: tuple) -> dict[str, str]:
    l3, q, aliases, short, detail, kw, diff = entry[:7]
    entities = DEFAULT_ENTITIES
    answer_format = "fact"
    region = "亚洲"
    era_start = era_end = ""
    fact_type = "bio"
    content_flags = ""
    tail = entry[7:]
    if tail and (tail[0].startswith("team:") or tail[0].startswith("player:")):
        entities = tail[0]
        tail = tail[1:]
    elif len(tail) > 0 and tail[0] == "":
        tail = tail[1:]
    if len(tail) > 0 and tail[0]:
        answer_format = tail[0]
    if len(tail) > 1:
        region = tail[1]
    if len(tail) > 2:
        era_start = tail[2]
    if len(tail) > 3:
        era_end = tail[3]
    if len(tail) > 4:
        fact_type = tail[4]
    if len(tail) > 5:
        content_flags = tail[5]
    return {
        "id": f"WC-PLCO-{seq:05d}",
        "category_l1": "球员与教练",
        "category_l2": CATEGORY_L2,
        "category_l3": l3,
        "scope": "world_cup",
        "priority": "4",
        "question": q,
        "question_aliases": aliases,
        "answer_short": short,
        "answer_detail": detail,
        "answer_format": answer_format,
        "keywords": kw,
        "tags": TAGS,
        "entities": entities,
        "related_ids": "",
        "difficulty": diff,
        "era_start": era_start,
        "era_end": era_end,
        "region": region,
        "language": "zh-CN",
        "fact_type": fact_type,
        "confidence": "official",
        "source_type": "historical_record",
        "source_ref": "FIFA World Cup official statistics",
        "content_flags": content_flags,
        "updated_at": "2026-06-04",
    }


def main() -> None:
    assert len(ENTRIES) == 50, f"expected 50 entries, got {len(ENTRIES)}"
    questions = [e[1] for e in ENTRIES]
    assert len(questions) == len(set(questions)), "duplicate questions"
    for e in ENTRIES:
        assert len(e[3]) <= 120, f"{e[0]}: answer_short too long ({len(e[3])})"
        assert len(e[5].split(",")) >= 3, e[0]

    new_rows = [row(START_ID + i - 1, e) for i, e in enumerate(ENTRIES, start=1)]
    existing: list[dict[str, str]] = []
    if OUTPUT.exists():
        with OUTPUT.open(newline="", encoding="utf-8") as f:
            existing = [dict(r) for r in csv.DictReader(f) if (r.get("id") or "").strip()]
    with OUTPUT.open("w", newline="", encoding="utf-8") as f:
        writer = csv.DictWriter(f, fieldnames=COLUMNS, lineterminator="\n")
        writer.writeheader()
        writer.writerows(existing)
        writer.writerows(new_rows)
    print(f"Wrote {len(new_rows)} rows (total {len(existing) + len(new_rows)})")


if __name__ == "__main__":
    main()
