def main() -> None:
    assert len(ENTRIES) == 60, f"expected 60 entries, got {len(ENTRIES)}"
    questions = [e[1] for e in ENTRIES]
    assert len(questions) == len(set(questions)), "duplicate questions"
    for e in ENTRIES:
        assert len(e[3]) <= 120, f"{e[0]}: answer_short too long ({len(e[3])})"
        assert len(e[5].split(",")) >= 3, e[0]

    new_rows = [row(START_ID + i - 1, e) for i, e in enumerate(ENTRIES, start=1)]
    existing: list[dict[str, str]] = []
    if OUTPUT.exists():
        with OUTPUT.open(newline="", encoding="utf-8") as f:
            existing = [dict(r) for r in csv.DictReader(f) if (r.get("id") or "").strip()]
    else:
        OUTPUT.parent.mkdir(parents=True, exist_ok=True)

    ids = {r["id"] for r in existing}
    for r in new_rows:
        if r["id"] in ids:
            raise SystemExit(f"duplicate id {r['id']}")

    with OUTPUT.open("w", newline="", encoding="utf-8") as f:
        writer = csv.DictWriter(f, fieldnames=COLUMNS, lineterminator="\n")
        writer.writeheader()
        writer.writerows(existing)
        writer.writerows(new_rows)
    print(f"Wrote {len(new_rows)} rows to {OUTPUT} (total {len(existing) + len(new_rows)})")


if __name__ == "__main__":
    main()
