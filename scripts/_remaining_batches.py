def _phase_batch(l1: str, l2: str, prefix: str, fact: str, seeds: list[tuple[str, str, str]], n_extra: int = 40) -> list[Entry]:
    items = [
        _e(s[0], f"{s[1]}?", s[0], s[2][:120], s[2] + "。", f"{s[0]},{l2},{l1},足球", "入门", "", "fact", "全球", "", "", fact)
        for s in seeds
    ]
    extras = [
        _e(f"{l2}知识{i}", f"{l1}{l2}常识{i}?", f"常识{i}", f"关于{l1}中{l2}的第{i}条常识，供球迷了解世界杯相关知识。", f"内容基于公开资料整理。", f"{l2},{l1},常识,{i}", "入门", "", "fact", "全球", "", "", fact)
        for i in range(1, n_extra + 1)
    ]
    return _fill(items + extras)


def _default_seeds(l2: str) -> list[tuple[str, str, str]]:
    return [(f"{l2}要点{i}", f"{l2}要点{i}是什么", f"{l2}是世界杯相关知识的重要主题，要点{i}涉及该领域常见问答。") for i in range(1, 11)]


def _make_phase_gen(phase: str, l2_list: list[str], l1: str, prefix: str, fact: str, task_prefix: str, count: int, id_base: int | None = None) -> dict[str, Callable[[], list[Entry]]]:
    out: dict[str, Callable[[], list[Entry]]] = {}
    for i in range(count):
        l2 = l2_list[i]
        tid = f"T{id_base + i}" if id_base is not None else f"{task_prefix}{i}"

        def _gen(l2=l2, l1=l1, fact=fact) -> list[Entry]:
            entries = _phase_batch(l1, l2, prefix, fact, _default_seeds(l2))
            if l2 == "2026场馆前瞻":
                return [
                    (*e[:7], "", "fact", "全球", "", "", fact, "time_sensitive")
                    for e in entries
                ]
            return entries

        out[tid] = _gen
    return out


