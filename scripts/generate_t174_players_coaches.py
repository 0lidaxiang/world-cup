def row(seq: int, entry: tuple) -> dict[str, str]:
    l3, q, aliases, short, detail, kw, diff = entry[:7]
    entities = DEFAULT_ENTITIES
    answer_format = "fact"
    region = "全球"
    era_start = era_end = ""
    fact_type = "stat"
    content_flags = ""
    tail = entry[7:]
    if tail and (tail[0].startswith("team:") or tail[0].startswith("player:") or tail[0].startswith("tournament:")):
        entities = tail[0]
        tail = tail[1:]
    elif len(tail) > 0 and tail[0] == "":
        tail = tail[1:]
    if len(tail) > 0 and tail[0]:
        answer_format = tail[0]
    if len(tail) > 1:
        region = tail[1]
    if len(tail) > 2:
        era_start = tail[2]
    if len(tail) > 3:
        era_end = tail[3]
    if len(tail) > 4:
        fact_type = tail[4]
