def row(seq: int, entry: tuple) -> dict[str, str]:
    l3, q, aliases, short, detail, kw, diff = entry[:7]
    entities = DEFAULT_ENTITIES
    answer_format = "fact"
    region = "南美"
    era_start = era_end = ""
    fact_type = "history"
    content_flags = ""
    tail = entry[7:]
    if tail and tail[0].startswith("team:"):
        entities = tail[0]
        tail = tail[1:]
    if len(tail) > 0:
        answer_format = tail[0]
    if len(tail) > 1:
        region = tail[1]
    if len(tail) > 2:
        era_start = tail[2]
